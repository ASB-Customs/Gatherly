// Renders a full Gatherly engagement report into a container.
import { esc, fmtLocal } from "/js/app.js";

const scoreColor = (s) => (s >= 70 ? "var(--good)" : s >= 45 ? "var(--signal)" : "var(--bad)");

function dial(score) {
  const r = 70, c = 2 * Math.PI * r, off = c * (1 - score / 100);
  return `
  <div class="score-dial">
    <svg viewBox="0 0 170 170" width="170" height="170">
      <circle cx="85" cy="85" r="${r}" fill="none" stroke="var(--line-strong)" stroke-width="10"/>
      <circle cx="85" cy="85" r="${r}" fill="none" stroke="${scoreColor(score)}" stroke-width="10"
        stroke-linecap="round" stroke-dasharray="${c}" stroke-dashoffset="${off}"/>
    </svg>
    <div class="val"><b style="color:${scoreColor(score)}">${score}</b><span>Health Score</span></div>
  </div>`;
}

function funnel(f) {
  const max = Math.max(f.views, 1);
  const rows = [["Listing views", f.views], ["Code reveals", f.reveals], ["Server entries", f.entries], ["Retained 30m+", f.retained30]];
  let html = `<div class="funnel">`;
  rows.forEach(([label, v], i) => {
    html += `<div class="funnel-row"><span>${label}</span>
      <div class="funnel-bar" style="width:${Math.max(2, (v / max) * 100)}%"></div>
      <b style="font-variant-numeric:tabular-nums">${v}</b></div>`;
    if (i < rows.length - 1) {
      const next = rows[i + 1][1];
      const drop = v > 0 ? Math.round((1 - next / v) * 100) : 0;
      if (drop > 0) html += `<div class="funnel-drop">&minus;${drop}% drop-off</div>`;
    }
  });
  return html + `</div>`;
}

function timeline(points) {
  if (!points?.length) return `<p class="note">No timeline data in this window.</p>`;
  const max = Math.max(...points.map((p) => p.n), 1);
  return `<div class="bars" style="margin-bottom:28px">${points.map((p) => `
    <div class="bar" style="height:${Math.max(4, (p.n / max) * 100)}%" title="${p.n} players">
      <i>${new Date(p.t).toLocaleTimeString([], { hour: "2-digit", minute: "2-digit" })}</i>
    </div>`).join("")}</div>`;
}

const momentumBadge = (m) => !m ? "" : m.direction === "up"
  ? `<span class="badge badge-good">Trending up +${m.changePct}%</span>`
  : m.direction === "down" ? `<span class="badge badge-bad">Declining ${m.changePct}%</span>` : `<span class="badge">Stable</span>`;

export function renderReport(el, r) {
  el.innerHTML = `
  <div class="card" style="margin-bottom:18px">
    <div style="display:flex;justify-content:space-between;gap:18px;flex-wrap:wrap;align-items:center">
      <div>
        <div style="display:flex;gap:10px;align-items:center;flex-wrap:wrap">
          <h3 style="font-size:1.5rem">${esc(r.eventTitle)}</h3>${momentumBadge(r.momentum)}
        </div>
        <p style="font-size:.88rem;margin-top:6px">${esc(r.serverName)} &middot; ${esc(r.scenario || "")} &middot; ${fmtLocal(r.windowStart)} - ${new Date(r.windowEnd).toLocaleTimeString([], { hour: "2-digit", minute: "2-digit" })}</p>
      </div>
      ${dial(r.score)}
    </div>
  </div>

  ${r.aiSummary ? `
  <div class="ai-summary" style="margin-bottom:18px">
    <div class="tag">Gatherly insight &middot; generated by Gatherly API</div>
    <p>${esc(r.aiSummary)}</p>
  </div>` : ""}

  <div class="grid grid-4" style="margin-bottom:18px">
    <div class="stat"><b>${r.joinsInWindow}</b><span>Joins in window</span></div>
    <div class="stat"><b>${r.peakConcurrent}</b><span>Peak concurrent</span></div>
    <div class="stat"><b>${r.avgSessionMin}m</b><span>Avg session length</span></div>
    <div class="stat"><b>${r.retained30}</b><span>Retained 30m+</span></div>
  </div>

  <div class="grid grid-2" style="margin-bottom:18px">
    <div class="card">
      <h3>Player lifecycle funnel</h3>
      <p style="font-size:.85rem;margin-bottom:14px">Where players were won and lost across the event.</p>
      ${funnel(r.funnel)}
      <p class="note" style="margin-top:14px">View-to-entry conversion: <b style="color:var(--text)">${r.conversionPct}%</b></p>
    </div>
    <div class="card">
      <h3>Players over the window</h3>
      <p style="font-size:.85rem;margin-bottom:14px">Concurrent players rebuilt from verified join and leave logs.</p>
      ${timeline(r.timeline)}
    </div>
  </div>

  <div class="grid grid-2" style="margin-bottom:18px">
    <div class="card">
      <h3>Scenario benchmark</h3>
      ${r.benchmark ? `
        <p style="font-size:.9rem;margin-top:8px">Against <b style="color:var(--text)">${r.benchmark.cohortSize}</b> ${esc(r.scenario || "same-scenario")} events on Gatherly in the last 30 days:</p>
        <div class="grid grid-2" style="margin-top:14px">
          <div class="stat"><b>${r.benchmark.peakPercentile}th</b><span>Percentile for peak concurrent</span></div>
          <div class="stat"><b>${r.benchmark.sessionPercentile}th</b><span>Percentile for session length</span></div>
        </div>
        <p class="note" style="margin-top:12px">Platform average session for this scenario: ${r.benchmark.platformAvgSessionMin}m. Yours: ${r.avgSessionMin}m.</p>`
      : `<p style="margin-top:8px">Benchmarks unlock once at least 3 other ${esc(r.scenario || "same-scenario")} events have reported in the last 30 days. The more the network grows, the sharper this gets.</p>`}
    </div>
    <div class="card">
      <h3>Next event forecast</h3>
      ${r.forecast ? `
        <p style="font-size:.9rem;margin-top:8px">Projected from your last ${r.forecast.basedOnEvents} reported events, weighted to the most recent:</p>
        <div class="grid grid-2" style="margin-top:14px">
          <div class="stat"><b>${r.forecast.projectedJoins[0]}-${r.forecast.projectedJoins[1]}</b><span>Projected joins</span></div>
          <div class="stat"><b>${r.forecast.projectedPeak[0]}-${r.forecast.projectedPeak[1]}</b><span>Projected peak</span></div>
        </div>
        ${r.forecast.recommendedStartLocal ? `<p class="note" style="margin-top:12px">Your best-performing start so far: ${fmtLocal(r.forecast.recommendedStartLocal)}. Consider holding that slot.</p>` : ""}`
      : `<p style="margin-top:8px">Forecasting unlocks after your third reported event. Gatherly then projects joins and peak attendance for your next session from your own history.</p>`}
    </div>
  </div>

  <div class="card" style="margin-bottom:18px">
    <h3>Staff intelligence</h3>
    <div class="grid grid-3" style="margin:14px 0 18px">
      <div class="stat"><b>${r.staffOnline}</b><span>Staff online (peak)</span></div>
      <div class="stat"><b>${r.staff?.avgModResponseMin ?? "-"}${r.staff?.avgModResponseMin != null ? "m" : ""}</b><span>Avg mod-call response</span></div>
      <div class="stat"><b>${r.modCalls}</b><span>Mod calls in window</span></div>
    </div>
    ${r.staff?.leaderboard?.length ? `
      <table class="tbl"><thead><tr><th>Staff member</th><th>Commands issued</th></tr></thead>
      <tbody>${r.staff.leaderboard.map((s) => `<tr><td>${esc(String(s.name).split(":")[0])}</td><td>${s.commands}</td></tr>`).join("")}</tbody></table>`
    : `<p class="note">No staff commands recorded in the window.</p>`}
    ${r.staff?.idle?.length ? `<p class="note" style="margin-top:12px">Online with zero commands: ${r.staff.idle.map(esc).join(", ")}.</p>` : ""}
  </div>

  <div style="display:flex;gap:12px;flex-wrap:wrap">
    <button class="btn btn-ghost btn-sm" id="shareCardBtn">Download share card (1200x630)</button>
    <span class="badge">Verified via the official ER:LC API</span>
    ${r.dmDelivered ? `<span class="badge badge-good">DM delivered</span>` : ""}
    ${r.webhookDelivered ? `<span class="badge badge-good">Webhook delivered</span>` : ""}
    ${r.recipientDelivered ? `<span class="badge badge-good">Recipient DM delivered</span>` : ""}
  </div>`;

  el.querySelector("#shareCardBtn")?.addEventListener("click", () => downloadShareCard(r));
}

export function downloadShareCard(r) {
  const c = document.createElement("canvas");
  c.width = 1200; c.height = 630;
  const x = c.getContext("2d");
  x.fillStyle = "#090d13"; x.fillRect(0, 0, 1200, 630);
  x.strokeStyle = "rgba(148,170,205,.14)";
  [90, 180, 270].forEach((rr) => { x.beginPath(); x.arc(1010, 315, rr, 0, 7); x.stroke(); });
  x.fillStyle = "#7fa8ff"; x.font = "600 26px sans-serif";
  x.fillText("GATHERLY · ENGAGEMENT REPORT", 70, 90);
  x.fillStyle = "#e9eef6"; x.font = "600 54px sans-serif";
  x.fillText(String(r.eventTitle).slice(0, 30), 70, 170);
  x.fillStyle = "#8e9aac"; x.font = "400 26px sans-serif";
  x.fillText(`${r.serverName} · ${r.scenario || ""}`, 70, 212);
  const col = r.score >= 70 ? "#69d99c" : r.score >= 45 ? "#7fa8ff" : "#ff7a7a";
  x.strokeStyle = "rgba(148,170,205,.28)"; x.lineWidth = 16;
  x.beginPath(); x.arc(1010, 315, 110, 0, 7); x.stroke();
  x.strokeStyle = col; x.lineCap = "round";
  x.beginPath(); x.arc(1010, 315, 110, -Math.PI / 2, -Math.PI / 2 + (r.score / 100) * 2 * Math.PI); x.stroke();
  x.fillStyle = col; x.font = "600 84px sans-serif"; x.textAlign = "center";
  x.fillText(String(r.score), 1010, 340);
  x.fillStyle = "#8e9aac"; x.font = "400 22px sans-serif";
  x.fillText("HEALTH SCORE", 1010, 380);
  x.textAlign = "left";
  const stats = [[r.joinsInWindow, "Joins"], [r.peakConcurrent, "Peak concurrent"], [`${r.avgSessionMin}m`, "Avg session"], [r.retained30, "Retained 30m+"]];
  stats.forEach(([v, l], i) => {
    const sx = 70 + (i % 2) * 330, sy = 320 + Math.floor(i / 2) * 130;
    x.fillStyle = "#e9eef6"; x.font = "600 56px sans-serif"; x.fillText(String(v), sx, sy);
    x.fillStyle = "#8e9aac"; x.font = "400 24px sans-serif"; x.fillText(l, sx, sy + 36);
  });
  x.fillStyle = "#5d6a78"; x.font = "400 22px sans-serif";
  x.fillText("Powered by Gatherly · verified via the official ER:LC API", 70, 580);
  const a = document.createElement("a");
  a.download = `gatherly-report-${(r.eventTitle || "event").replace(/\W+/g, "-").toLowerCase()}.png`;
  a.href = c.toDataURL("image/png");
  a.click();
}
